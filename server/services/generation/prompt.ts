export function buildConsistencyCheckPrompt(
  chaptersText: string,
  foreshadowSummary: string,
  characterSummary: string,
  chapterRange: string
): string {
  let prompt = `请对以下章节(${chapterRange})进行一致性审阅。不需要重写或评价文笔，只检查以下三类问题：

## 检查项目

### 1. 角色一致性
- 角色行为是否与前文设定矛盾？（如：胆小的人突然勇敢但没有成长过程）
- 角色的位置、能力、关系是否有不合理的跳跃？
- 是否有角色"消失"——在前文中出现但没有交代去向？

### 2. 情节连贯性
- 相邻章节之间是否有逻辑跳跃或时间线矛盾？
- 是否有事件在前文中已经发生过但被当作新事件重述？
- 数字、名称、地点是否有前后不一致？

### 3. 伏笔和坑
- 是否有前文埋下的伏笔在当前章节中被遗忘？
- 是否有新埋的伏笔看起来像是临时拼凑的？

## 参考信息

`;

  // Inject foreshadow status from framework-extracted summary
  if (foreshadowSummary) {
    prompt += `### 伏笔状态\n${foreshadowSummary}\n`
  }

  // Inject character status from framework-extracted summary
  if (characterSummary) {
    prompt += `### 角色状态\n${characterSummary}\n`
  }

  prompt += `## 待审阅章节内容\n\n${chaptersText}\n\n`
  prompt += `## 输出格式
请按以下格式输出审阅结果：

【审阅结果】
✅ 通过项：（列出没有问题的方面）
⚠️ 轻微问题：（列出值得注意但不严重的问题，给出具体章和段落）
❌ 严重问题：（列出必须修改的问题——角色严重OOC、情节逻辑矛盾、时间线混乱）

如果全部通过，只需输出"本次无严重一致性问题"并简述通过项。`
  return prompt
}
